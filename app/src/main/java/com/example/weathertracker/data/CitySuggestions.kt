// Function to filter city suggestions based on search query
fun filterCitySuggestions(query: String, limit: Int = 10): List<CitySuggestion> {
    if (query.isBlank()) return emptyList()
    
    val lowercaseQuery = query.lowercase()
    return CITY_SUGGESTIONS
        .filter { 
            it.name.lowercase().contains(lowercaseQuery) || 
            it.countryName.lowercase().contains(lowercaseQuery)
        }
        .take(limit)
}
